// This is a single-line comment. Program execution ignores comments.
package main // Declares the package name - 'main' is required for executables.

import "fmt" // Imports the 'fmt' package, which provides functions for formatted I/O (like printing).

// main is the function where program execution begins.
func main() {
	// Println is a function from the 'fmt' package that prints a line of text to the console.
	fmt.Println("Hello, World!") // We're telling it to print the string "Hello, World!".
}
